package devetlopers.finhealth;

public class User {

    private String name;
    private String surname;
    private int zostatok;

    public User() {
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getZostatok() {
        return zostatok;
    }

    public void setZostatok(int zostatok) {
        this.zostatok = zostatok;
    }
}
